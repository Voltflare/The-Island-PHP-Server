    <h2>The Followers of the Sword:</h2>
    <p>
    The Followers of the Sword are a cult of Pokemon that have, out of a desperation not to be killed off by their "leader", pledged their undying allegiance to the Black Knight. Originally, this vision sprouted out of a small group of Pokemon in 42 BKR/AM who did not want to be publicly executed by the Mafia, and have since signed contracts with Krueger, himself (via messages to and from his Mafia, at least- not a single Follower has actually seen Kreuger, before, so they do not know that he is the Black Knight), to hunt down those that would plague him. Initially, this partnership was just used to keep the peace and help forcibly quell doubts of his legitimacy (along with his Mafia, of course), but after Guilds began to crop up, the Black Knight found a new target, and he took this opportunity to vehemently turn the Followers against the Guilds- with the promise that he would reward them handsomely upon the last Guild Pokemon's execution.

    <br> <br> The Followers, essentially being the Slaking's bootlickers, are often contrasted with the equally-extreme Guilds. Depending on who you ask, both sides could be errant in fighting for their cause, and thus, views on the two groups are commonly disputed over which is correct. Some Pokemon enjoy the company that the battle-bred Followers bring, as while they are a bit crazy when it comes to their elaborate, gory rituals, they do not tend to violently sabotage the other "party's" work through physical or mental means like the Guilds; and others will claim that the Guilds' cause is just, as they are fighting for the same thing that everyone wants- to reunite with the mainland- while the Followers are just a bunch of delusional crazies that get off to stabbing themselves in the foot. 

    </p>

