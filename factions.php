    <h2>Guilds of the Island:</h2>
    <p>
    A phenomenon that most Pokemon on the Island are familiar with is that of a team- though, traditionally this partnership took place between a Tamer and their Pokemon, without them- a “team” takes on a whole new meaning when applied to the mundane task of survival, something that Pokemon typically are not tasked with on their own when they’re under the care of a Tamer.
    <br><br>These Guilds serve to harken back to these “older” customs in more ways than one. Literally the most “religious” presence on the Island, Guild Pokemon, as they are called, serve one master and one master alone- the Deity herself. The identities of their members are kept secret to everyone but their own ranks - because those that are discovered to be Guild members… well, let’s just say they don’t exactly have the greatest lives for the remaining few days they have left before the Knighted Mafia finds them. Even fellow Guild members are liable to turn on a “discovered” Guild Pokemon, typically creating martyrs that both serve to further inform the surviving Guild members of the situation’s gravity as well as further igniting the collective fire in their hearts against the Donkey Kong wannabe that continues to hold nonexistent power over their heads. 
    <br><br>Despite this anger, though, it is not often that Guild Pokemon lash out against the Black Knight’s forces- those of which are known of, at least- due to this heavy risk. As there are only two Guilds that have not been routed by the infernal Mafia, Guilds do not have potential resources and recruits that they can freely throw away in striking back against the hidden tyrant they want to eventually overthrow. They do this as being accused of being a Guild Pokemon in front of the wrong set of ears can very easily lead to having a target painted on your back and very many unwanted glares from all for the rest of your life.
    <br><br> Two different Guilds exist within the confines of the Island- the <strong>Bastion of Marble</strong> and the <strong>Gallant Glyphs</strong>. 

    </p>

    <h5>The Bastion of Marble:</h5>
    <p>The Bastion of Marble (also known as the Granite Gabion) are typically hardier Pokemon with a penchant for knowledge and debate- and prioritize misdirection, opting to indirectly setting up situations that can further cast doubt on the Black Knight instead of forcing it into them. Of the two, this Guild is much more hidden, but as such, the Pokemon are trained to hide, not fight- and they do a pretty good job of it. They are normally more survivalists, which can give them an edge in the wilderness over even the mighty Mizanite City-dwellers (who are typically famed for their survivability and stoutness). The headquarters of the Bastion is hidden within the bowels of the Amber Bypass, where the clutches of darkness will never hope to find it amidst rock and clay.</p>
    <h5>The Gallant Glyphs:</h5>
    <p>The Gallant Glyphs take a more concrete approach to their misdirection- forcibly using their mastery of telekinetics to obfuscate and scramble the minds of those that have yet to see the truth. Of course, this gives the Glyphs an actual reputation instead of the Bastion- which is both a blessing and a curse. On one hand, it is advised to stay away from the Glyphs at all costs (and bedtime stories about the Glyphs are about as present as those of the Black Knight) due to this hearsay. Instead of hiding and silently scheming their way into recruiting others, the Glyphs are much more forceful in their approach to combatting the Black Knight’s influence. These Pokemon study their entire lives to fight battles both in the mind and on the battlefield, and thus are typically much better prepared to fight as opposed to the members of the Bastion. Their headquarters are located far beneath the surface of Mizanite City- where the only ways in and out are a jury-rig teleportation that acts as a makeshift elevator or somehow digging your way through hundreds of feet of earth to find them.</p>

