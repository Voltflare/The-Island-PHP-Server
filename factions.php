    <h5>The Knighted Mafia:</h5>
    <p>
    In a word, corrupt. The Knighted Mafia are effectively Kreuger’s private police force, expertly trained assassins with exactly one single prerogative: to eliminate any and all traces of the Guilds- Pokemon, documents, buildings and homes, or otherwise. They know no fear, have no morals, do not speak (in fact, none of them even have tongues to speak with, even if they deigned to do so), and will stop at nothing to perfectly carry out any order received from their Knight.

    <br><br>Each member of the original Knighted Mafia was hand-picked by the king, himself (before they even had a collective name). Before Kreuger’s transformation into the Black Knight, they were actually quite the lawful bunch- acting more like glorified natural disaster relief squads than full-blown mercenaries and assassins. But, that all changed when the Black Knight showed his true, ugly colors to the world, and the impossible-to-miss head ornaments came on. Noted only by their distinctive black, pointed hats, the Mafia quickly became one of the most recognized and feared organizations of Pokemon on the Island, as anyone who dared disagree with the Knight’s wishes was very quickly and unceremoniously disposed of. Simply put, you did not wrong the Knight or his Mafia and live to tell the tale.
    </p>

